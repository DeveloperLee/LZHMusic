package com.lzh.model;

public class LyricsItem {
	
	   private String content;
	   private int time;
	   public LyricsItem(){
		   
	   }
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	   

}
